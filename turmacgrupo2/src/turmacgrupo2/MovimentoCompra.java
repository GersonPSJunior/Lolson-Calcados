public String atualizarEntrada(DetCompra detCompra,Estoque estoque)
       {
            Conexao c=new Conexao();
            try
                {
                    String sentenca;
                    int saldoAtual=estoque.getSaldo()+detCompra.getQtdVenda();
                    sentenca = "UPDATE turmacgrupo2.estoque SET `qtde` = " + detCompra.getQtdVenda()+", `tipo` = 'E',  `valor` = "+detCompra.getValorTotal()+",`saldo` = "+ saldoAtual+" WHERE `descricao` ='"+ detCompra.getDescricaoProduto()+"'";
                    return c.atualizarBanco(sentenca);
                }
            catch(Exception e)
                {
                    return e.getMessage();
                }
        }
