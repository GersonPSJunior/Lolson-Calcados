    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Double valorTotal= 0.0;
       Double totalGeral= 0.0;
       
       int index = masterTable.getSelectedRow();
        turmacgrupo2.Compra c = list.get(masterTable.convertRowIndexToModel(index));
        Collection<turmacgrupo2.DetCompra> ds = c.getDetCompraList();
        
        for (DetCompra detCompra : ds) {
            if(detCompra.getQtdVenda()== null){
                detCompra.setQtdVenda(1);
            }
            detCompra.setValorTotal(detCompra.getValorUnitario()* detCompra.getQtdVenda());
            
            valorTotal = valorTotal + detCompra.getValorTotal();
        }
        if(jTextField7.getText().equals("")){
            jTextField7.setText("0");
        }
        
        totalGeral = valorTotal - Double.parseDouble(jTextField7.getText());
        
        jTextField6.setText(String.valueOf(valorTotal));
        jTextField8.setText(String.valueOf(totalGeral));
        jButton4ActionPerformed(null);
    }//GEN-LAST:event_jButton1ActionPerformed

