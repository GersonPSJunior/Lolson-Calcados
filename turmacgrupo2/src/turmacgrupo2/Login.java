    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        query=entityManager.createNamedQuery("Funcionario.verificaLogin");
        query.setParameter("login", jTextField1.getText());
        query.setParameter("senha", jPasswordField1.getText());
        List <Funcionario> data = query.getResultList();
        if(data.size() > 0)
        {
            String args[] = new String[2];
            args[0] = data.get(0).getIdFuncionario().toString();
            args[1] = data.get(0).getNome();
            Menu.main(args);
            dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Usuario ou senha incorretos!");
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

