    private void deleteDetailButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteDetailButtonActionPerformed
        int index = masterTable.getSelectedRow();
        turmacgrupo2.Venda v = list.get(masterTable.convertRowIndexToModel(index));
        Collection<turmacgrupo2.DetVenda> ds = v.getDetVendaList();
        int[] selected = detailTable.getSelectedRows();
        List<turmacgrupo2.DetVenda> toRemove = new ArrayList<turmacgrupo2.DetVenda>(selected.length);
        for (int idx = 0; idx < selected.length; idx++) {
            selected[idx] = detailTable.convertRowIndexToModel(selected[idx]);
            int count = 0;
            Iterator<turmacgrupo2.DetVenda> iter = ds.iterator();
            while (count++ < selected[idx]) {
                iter.next();
            }
            turmacgrupo2.DetVenda d = iter.next();
            toRemove.add(d);
            entityManager.remove(d);
        }
        ds.removeAll(toRemove);
        masterTable.clearSelection();
        masterTable.setRowSelectionInterval(index, index);
    }//GEN-LAST:event_deleteDetailButtonActionPerformed
    
    private void newDetailButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newDetailButtonActionPerformed
        saveButtonActionPerformed(null);
        int index = masterTable.getSelectedRow();
        turmacgrupo2.Venda v = list.get(masterTable.convertRowIndexToModel(index));
        Collection<turmacgrupo2.DetVenda> ds = v.getDetVendaList();
        if (ds == null) {
            ds = new LinkedList<turmacgrupo2.DetVenda>();
            v.setDetVendaList((List) ds);
        }
        turmacgrupo2.DetVenda d = new turmacgrupo2.DetVenda();
        DetVendaPK vpk = new DetVendaPK();
        vpk.setVendaidVenda(v.getIdVenda());
        d.setDetVendaPK(vpk);
        ImportaProduto ip=new ImportaProduto(null, true);
        ip.recebeObjeto(d);
        ip.setVisible(true);
        queryProduto = entityManager.createNamedQuery("Produto.findByIdProduto");
        queryProduto.setParameter("idProduto",d.getDetVendaPK().getProdutoidProduto());
        List <Produto> data;
        data = queryProduto.getResultList();
        d.setDescricaoProduto(data.get(0).getDescricao());        
        entityManager.persist(d);
        d.setVenda(v);
        ds.add(d);
        masterTable.clearSelection();
        masterTable.setRowSelectionInterval(index, index);
        int row = ds.size() - 1;
        detailTable.setRowSelectionInterval(row, row);
        detailTable.scrollRectToVisible(detailTable.getCellRect(row, 0, true));
        jTextField10.requestFocus();
    }//GEN-LAST:event_newDetailButtonActionPerformed
    

       
