public String atualizarSaida(DetVenda detVenda,Estoque estoque)
       {
            Conexao c=new Conexao();
            try
                {
                    String sentenca;
                    int saldoAtual= estoque.getSaldo()-detVenda.getQtdeVenda();
                    sentenca = "UPDATE turmacgrupo2.estoque SET `qtde` = " + detVenda.getQtdeVenda()+", `tipo` = 'S',  `valor` = "+detVenda.getValorTotal()+",`saldo` = "+ saldoAtual+" WHERE `descricao` ='"+ detVenda.getDescricaoProduto()+"'";
                    return c.atualizarBanco(sentenca);
                }
            catch(Exception e)
                {
                    return e.getMessage();
                }
        }
