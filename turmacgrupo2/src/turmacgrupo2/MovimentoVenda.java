    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Double valorTotal = 0.0;
        Double totalGeral = 0.0;
        
        int index = masterTable.getSelectedRow();
        turmacgrupo2.Venda v = list.get(masterTable.convertRowIndexToModel(index));
        Collection<turmacgrupo2.DetVenda> ds = v.getDetVendaList();
        
        for (DetVenda detVenda : ds) {
            if(detVenda.getQtdeVenda()==null){
                detVenda.setQtdeVenda(1);
            }
            detVenda.setValorTotal(detVenda.getValorUnitario() * detVenda.getQtdeVenda());
            valorTotal+=detVenda.getValorTotal();
        }
        if(jTextField7.getText().equals(""))
        {
            jTextField7.setText("0");
        }
        totalGeral=valorTotal-Double.parseDouble(jTextField7.getText());
        
        
        jTextField6.setText(String.valueOf(valorTotal));
        jTextField9.setText(String.valueOf(totalGeral));
        jButton4ActionPerformed(null);
        
    }//GEN-LAST:event_jButton1ActionPerformed

